nambari = 9
nambari2 = 4

# mathematical operators
print(nambari2 + nambari)
print(nambari2 - nambari)
print(nambari2 * nambari)
print(nambari2 / nambari)
print(nambari2 % nambari)

# comparison operatos
print(f"{nambari} is equal to {nambari2}: {nambari == nambari2}")
print(f"{nambari} is greater than {nambari2}: {nambari > nambari2}")
print(f"{nambari} is lesser than {nambari2}: {nambari < nambari2}")
print(f"{nambari} is lesser than or equals {nambari2}: {nambari <= nambari2}")
print(f"{nambari} is greater than or equals {nambari2}: {nambari >= nambari2}")
print(f"{nambari} is not equal to {nambari2}: {nambari != nambari2}")

# logical operators
num2 = 34
print(f"both statements should be true: {num2 == 34 and num2 > 39}")
print(f"one statements should be true: {num2 == 34 and num2 > 39}")

# assignment operator

num3 = 27
num4 = 8
print("num3 + num4 =", num3 == num4)
print("num3 += num4 =", num3 + num4)
print("num3 -= num4 =", num3 - num4)
